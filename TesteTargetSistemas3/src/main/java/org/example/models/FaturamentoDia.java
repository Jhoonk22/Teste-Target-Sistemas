package org.example.models;

public class FaturamentoDia {
    String dia;
    double valor;

    public void setDia(String dia) {
        this.dia = dia;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }

    public String getDia() {
        return dia;
    }

    public double getValor() {
        return valor;
    }
}
